package ten.prinprin;

public class Officer {
    private String macb;
    private String hoten;

    public Officer(String macb, String hoten) {
        this.macb = macb;
        this.hoten = hoten;
    }

    public String getMacb() {
        return macb;
    }

    public String getHoten() {
        return hoten;
    }
}
